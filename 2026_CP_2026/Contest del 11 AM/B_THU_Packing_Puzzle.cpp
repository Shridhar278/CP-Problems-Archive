    int n_u = max(u-t, (int)0);
    t=n_t; u=n_u;
    ans+=u*3;
    ans+=(min(t, 2*h)/2)*7;
    int x = (min(t, 2*h)/2);
    n_t = max(t-2*x, (int)0);
    int n_h = max(h-x, (int)0);
    t=n_t; h=n_h;
    if (h>=1 and t==1) {
        ans+=5;
        h--; t=0;
    }
    ans+=h*3;
