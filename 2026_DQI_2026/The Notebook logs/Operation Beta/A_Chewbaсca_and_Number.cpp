int fast_exp(int base, int exp) {
    int res = 1;
    while (exp > 0) {
        if (exp % 2 == 1) res *= base; // If odd, multiply with result
        base *= base;                  // Square the base
        exp /= 2;                      // Divide exponent by 2
    }
    return res;
}

